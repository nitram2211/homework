package zad4temp;

public class zad4temp {

	public static void main(String[] args) {
		int a = 1;
		int b = 3;

		System.out.println("Before swapping two variables a: " + a + " b: " + b);
		int temp = a;
		a = b;
		b = temp;

		System.out.println("After swapping two variables a: " + a + " b: " + b);

	}

}
